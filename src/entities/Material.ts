import {
    Entity,
    PrimaryColumn,
    Column,
    CreateDateColumn,
    UpdateDateColumn,
  } from "typeorm";
  import { v4 as uuidV4 } from "uuid";
  
  @Entity("materiais")
  class Material {
    @PrimaryColumn()
    readonly id: string;
  
    @Column()
    nome: string;

    @CreateDateColumn()
    created_at: Date;
  
    @UpdateDateColumn()
    updated_at: Date;
  
    constructor() {
      if (!this.id) {
        this.id = uuidV4();
      }
    }
  }
  
  export { Material };